// AI 추천 프롬프트에 주입할 기준 텍스트 생성
export function criteriaToPromptText() {
  return Object.values(SDLC_FACTOR_CRITERIA)
    .map(f => `[${f.label}] ${Object.entries(f.options).map(([k, v]) => `${k}=${v}`).join(' / ')}`)
    .join('\n');
}
